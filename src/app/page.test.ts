    expect(source).toContain("Launch Target Meter");
    expect(source).toContain("25-unit target + value mix");
    expect(source).toContain("Build Capacity by Build");
    expect(source).toContain("Buildable units by active build");
    expect(source).not.toContain("Package Bottlenecks");
    expect(source).not.toContain("buildable as subassembly");
    expect(source).not.toContain("buildableSubassemblyCapacity");
    expect(source).toContain("Stock Pressure");
    expect(source).toContain("Operations Flow");
    expect(source).toContain("Attention preview");
    expect(source).toContain("human-approval-queue");
    expect(source).not.toContain("Observed weighted avg");
    expect(source).toContain("bg-slate-950/95");
    expect(source).toContain("catalog/default");
    expect(source).toContain("Value mix");
    expect(source).toContain("summary.dashboardGraphs");
    expect(source).not.toContain("Open Next Actions");
    expect(source).not.toContain("summary.launchReadiness.nextActions.map");
    expect(source).toContain("LaunchTargetAndValueMixGraph");
    expect(source).toContain("LaunchCoverageBar");
