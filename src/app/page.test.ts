    expect(source).toContain("Reserved");
    expect(source).toContain("Available");
    expect(source).toContain("Phase I Launch Readiness");
    expect(source).toContain("25-unit Phase I target");
    expect(source).toContain("packageDisplayName");
    expect(source).not.toContain("LAMBENTI_PACKAGE BOM capacity");
    expect(source).toContain("subassemblies must already be ledger-built");
